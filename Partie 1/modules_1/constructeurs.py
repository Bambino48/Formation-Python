"""
Les types natifs de bases peuvent être créé à partitr de leurs classes.

    * str() → Chaînes de caratères
    * int() → Nombres entiers
    * float() → Nombres décimaux
    * bool() → Booléens

Ce sont des fonctions de conversions
"""
print(str("Hello"))
print(int(5))
print(float(10.7))
print(bool(True))

string = str(10)
print(string)

integer = int(10)
print(integer)
